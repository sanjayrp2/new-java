import java.util.*;
public class addition {
    public static void main(String ar[]){
        Scanner sc=new Scanner(System.in);
        int n=sc.nextInt();
        int m=sc.nextInt();
        int a=0;
        for(int i=1;i<=n;i++){
            a=a+m;
        }
        System.out.println(a);
    }
    
}
